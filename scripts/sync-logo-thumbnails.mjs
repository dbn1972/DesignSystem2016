import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const ROOT = 'https://www.ux4g.gov.in';
const BASE = 'https://www.ux4g.gov.in/logos';
const DATA_PATH = path.join(__dirname, '..', 'src', 'app', 'content', 'logo-data.ts');
const OUTPUT_PATH = path.join(__dirname, '..', 'src', 'app', 'content', 'logo-thumbnails.ts');

const GENERIC_ASSET_NAMES = new Set([
  'eps.png',
  'gif.png',
  'svg.png',
  'png.png',
  'jpg.png',
  'jpeg.png',
  'pdf.png',
  'zip.png',
]);

function parseEntries(source) {
  const entryRegex =
    /\{\s*id:\s*'([^']+)'[\s\S]*?sourceUrl:\s*(?:`([^`]+)`|'([^']+)')[\s\S]*?\}/g;
  const entries = [];

  for (const match of source.matchAll(entryRegex)) {
    const id = match[1];
    const sourceExpr = match[2] ?? match[3] ?? '';
    const sourceUrl = sourceExpr.replaceAll('${BASE}', BASE);

    entries.push({ id, sourceUrl });
  }

  return entries;
}

function extractAssetLinks(html) {
  return [...html.matchAll(/(?:src|href)=['"]([^'"]+)['"]/gi)].map((match) => match[1]);
}

function isValidThumbnailCandidate(assetPath) {
  const filename = assetPath.split('/').pop()?.toLowerCase() ?? '';
  const isRasterOrVectorPreview = /\.(png|jpe?g|svg|webp)$/i.test(assetPath);
  const isKnownLogoDirectory =
    assetPath.includes('/assets/img/uxdt-logo/') || assetPath.includes('/assets/img/logos-page/');

  if (!isRasterOrVectorPreview || !isKnownLogoDirectory) {
    return false;
  }

  if (GENERIC_ASSET_NAMES.has(filename)) {
    return false;
  }

  if (assetPath.includes('/assets/img/logos-page/') && /\.(zip|pdf)$/i.test(assetPath)) {
    return false;
  }

  return true;
}

function extractThumbnailUrl(html) {
  const assetLinks = extractAssetLinks(html);
  const previewCandidate = assetLinks.find(
    (assetPath) => assetPath.includes('/assets/img/uxdt-logo/') && isValidThumbnailCandidate(assetPath),
  );

  if (previewCandidate) {
    return new URL(previewCandidate, ROOT).toString();
  }

  const logoPageCandidate = assetLinks.find((assetPath) => isValidThumbnailCandidate(assetPath));
  if (logoPageCandidate) {
    return new URL(logoPageCandidate, ROOT).toString();
  }

  return undefined;
}

function renderOutput(thumbnails) {
  const lines = [
    '/**',
    ' * Generated by scripts/sync-logo-thumbnails.mjs',
    ' * Source: https://www.ux4g.gov.in/logo.php and detail pages under /logos',
    ' * Do not edit manually.',
    ' */',
    '',
    'export const LOGO_THUMBNAILS: Record<string, string> = {',
  ];

  for (const [id, url] of Object.entries(thumbnails)) {
    lines.push(`  '${id}': '${url}',`);
  }

  lines.push('};', '');
  return lines.join('\n');
}

async function fetchHtml(url) {
  const response = await fetch(url, {
    headers: {
      'user-agent': 'UX4G Logo Sync/1.0',
      accept: 'text/html,application/xhtml+xml',
    },
  });

  if (!response.ok) {
    throw new Error(`HTTP ${response.status} for ${url}`);
  }

  return response.text();
}

async function main() {
  const source = fs.readFileSync(DATA_PATH, 'utf8');
  const entries = parseEntries(source);
  const thumbnails = {};
  const failures = [];

  for (const entry of entries) {
    try {
      const html = await fetchHtml(entry.sourceUrl);
      const thumbnailUrl = extractThumbnailUrl(html);

      if (thumbnailUrl) {
        thumbnails[entry.id] = thumbnailUrl;
      } else {
        failures.push(`${entry.id}: no thumbnail asset found`);
      }
    } catch (error) {
      failures.push(`${entry.id}: ${error.message}`);
    }
  }

  fs.writeFileSync(OUTPUT_PATH, renderOutput(thumbnails), 'utf8');

  console.log(`Wrote ${Object.keys(thumbnails).length} logo thumbnails to ${path.relative(process.cwd(), OUTPUT_PATH)}`);

  if (failures.length > 0) {
    console.log('');
    console.log('Missing or failed entries:');
    for (const failure of failures) {
      console.log(`- ${failure}`);
    }
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
