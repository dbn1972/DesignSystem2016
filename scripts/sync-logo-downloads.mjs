import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const ROOT = 'https://www.ux4g.gov.in';
const BASE = 'https://www.ux4g.gov.in/logos';
const DATA_PATH = path.join(__dirname, '..', 'src', 'app', 'content', 'logo-data.ts');
const OUTPUT_PATH = path.join(__dirname, '..', 'src', 'app', 'content', 'logo-downloads.ts');

const FORMAT_LABELS = {
  eps: 'EPS (ZIP)',
  gif: 'GIF (ZIP)',
  svg: 'SVG (ZIP)',
  png: 'PNG (ZIP)',
  jpg: 'JPG (ZIP)',
  jpeg: 'JPEG (ZIP)',
  pdf: 'PDF',
  zip: 'ZIP',
};

function parseEntries(source) {
  const entryRegex =
    /\{\s*id:\s*'([^']+)'[\s\S]*?sourceUrl:\s*(?:`([^`]+)`|'([^']+)')[\s\S]*?\}/g;
  const entries = [];

  for (const match of source.matchAll(entryRegex)) {
    const id = match[1];
    const sourceExpr = match[2] ?? match[3] ?? '';
    const sourceUrl = sourceExpr.replaceAll('${BASE}', BASE);
    entries.push({ id, sourceUrl });
  }

  return entries;
}

function inferFormat(assetPath) {
  const normalized = assetPath.toLowerCase();
  if (normalized.includes('-svg')) return 'svg';
  if (normalized.includes('-png')) return 'png';
  if (normalized.includes('-eps')) return 'eps';
  if (normalized.includes('-gif')) return 'gif';
  if (normalized.includes('-jpg')) return 'jpg';
  if (normalized.includes('-jpeg')) return 'jpeg';
  if (normalized.endsWith('.pdf')) return 'pdf';
  if (normalized.endsWith('.zip')) return 'zip';

  const extension = normalized.split('.').pop();
  if (extension && extension in FORMAT_LABELS) {
    return extension;
  }

  return 'zip';
}

function titleForFormat(format) {
  return FORMAT_LABELS[format] ?? format.toUpperCase();
}

function extractDownloads(html) {
  const assetSet = new Set(
    [...html.matchAll(/data-file-path=['"]([^'"]+)['"]/gi)].map((match) => match[1]),
  );

  return [...assetSet]
    .filter((assetPath) => /assets\/img\/logos-page\//i.test(assetPath))
    .map((assetPath) => {
      const format = inferFormat(assetPath);
      return {
        format,
        label: titleForFormat(format),
        url: new URL(assetPath, ROOT).toString(),
      };
    })
    .sort((left, right) => left.label.localeCompare(right.label));
}

function renderOutput(downloads) {
  const lines = [
    '/**',
    ' * Generated by scripts/sync-logo-downloads.mjs',
    ' * Source: https://www.ux4g.gov.in/logo.php and detail pages under /logos',
    ' * Do not edit manually.',
    ' */',
    '',
    "import type { LogoDownloadAsset } from './logo-data';",
    '',
    'export const LOGO_DOWNLOADS: Record<string, LogoDownloadAsset[]> = {',
  ];

  for (const [id, assets] of Object.entries(downloads)) {
    lines.push(`  '${id}': [`);
    for (const asset of assets) {
      lines.push(
        `    { format: '${asset.format}', label: '${asset.label}', url: '${asset.url}' },`,
      );
    }
    lines.push('  ],');
  }

  lines.push('};', '');
  return lines.join('\n');
}

async function fetchHtml(url) {
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), 10000);
  let response;

  try {
    response = await fetch(url, {
      signal: controller.signal,
      headers: {
        'user-agent': 'UX4G Logo Download Sync/1.0',
        accept: 'text/html,application/xhtml+xml',
      },
    });
  } finally {
    clearTimeout(timeout);
  }

  if (!response.ok) {
    throw new Error(`HTTP ${response.status} for ${url}`);
  }

  return response.text();
}

async function main() {
  const source = fs.readFileSync(DATA_PATH, 'utf8');
  const entries = parseEntries(source);
  const downloads = {};
  const failures = [];

  for (const entry of entries) {
    try {
      const html = await fetchHtml(entry.sourceUrl);
      const assets = extractDownloads(html);

      if (assets.length > 0) {
        downloads[entry.id] = assets;
      } else {
        failures.push(`${entry.id}: no download assets found`);
      }
    } catch (error) {
      failures.push(`${entry.id}: ${error.message}`);
    }
  }

  fs.writeFileSync(OUTPUT_PATH, renderOutput(downloads), 'utf8');

  console.log(
    `Wrote ${Object.keys(downloads).length} logo download sets to ${path.relative(process.cwd(), OUTPUT_PATH)}`,
  );

  if (failures.length > 0) {
    console.log('');
    console.log('Missing or failed entries:');
    for (const failure of failures) {
      console.log(`- ${failure}`);
    }
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
