import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const ROOT = path.join(__dirname, '..');
const SOURCE_PATH = path.join(ROOT, 'src', 'app', 'content', 'logo-downloads.ts');
const OUTPUT_DIR = path.join(ROOT, 'public', 'assets', 'logo-downloads');
const OUTPUT_MAP_PATH = path.join(ROOT, 'src', 'app', 'content', 'logo-local-downloads.ts');

function parseDownloads(source) {
  const downloads = {};
  const blockRegex = /'([^']+)': \[(.*?)\],/gs;
  const assetRegex =
    /\{\s*format:\s*'([^']+)',\s*label:\s*'([^']+)',\s*url:\s*'([^']+)'\s*\}/g;

  for (const blockMatch of source.matchAll(blockRegex)) {
    const id = blockMatch[1];
    const block = blockMatch[2];
    const assets = [];

    for (const assetMatch of block.matchAll(assetRegex)) {
      assets.push({
        format: assetMatch[1],
        label: assetMatch[2],
        url: assetMatch[3],
      });
    }

    if (assets.length) {
      downloads[id] = assets;
    }
  }

  return downloads;
}

function localPathFor(id, assetUrl) {
  const filename = assetUrl.split('/').pop();
  return {
    abs: path.join(OUTPUT_DIR, id, filename),
    rel: `/assets/logo-downloads/${id}/${filename}`,
  };
}

function renderOutput(localDownloads) {
  const lines = [
    '/**',
    ' * Generated by scripts/mirror-logo-downloads.mjs',
    ' * Do not edit manually.',
    ' */',
    '',
    "import type { LogoDownloadAsset } from './logo-data';",
    '',
    'export const LOGO_LOCAL_DOWNLOADS: Record<string, LogoDownloadAsset[]> = {',
  ];

  for (const [id, assets] of Object.entries(localDownloads)) {
    lines.push(`  '${id}': [`);
    for (const asset of assets) {
      lines.push(
        `    { format: '${asset.format}', label: '${asset.label}', url: '${asset.url}' },`,
      );
    }
    lines.push('  ],');
  }

  lines.push('};', '');
  return lines.join('\n');
}

async function downloadFile(url, destination) {
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), 15000);

  try {
    const response = await fetch(url, {
      signal: controller.signal,
      headers: {
        'user-agent': 'UX4G Logo Mirror/1.0',
      },
    });

    if (!response.ok) {
      throw new Error(`HTTP ${response.status}`);
    }

    const bytes = Buffer.from(await response.arrayBuffer());
    if (bytes.length < 100) {
      throw new Error('File too small');
    }

    fs.mkdirSync(path.dirname(destination), { recursive: true });
    fs.writeFileSync(destination, bytes);
    return true;
  } finally {
    clearTimeout(timeout);
  }
}

async function main() {
  const source = fs.readFileSync(SOURCE_PATH, 'utf8');
  const downloads = parseDownloads(source);
  const localDownloads = {};
  const failures = [];
  let count = 0;

  fs.mkdirSync(OUTPUT_DIR, { recursive: true });

  for (const [id, assets] of Object.entries(downloads)) {
    for (const asset of assets) {
      const paths = localPathFor(id, asset.url);

      try {
        if (!fs.existsSync(paths.abs) || fs.statSync(paths.abs).size === 0) {
          await downloadFile(asset.url, paths.abs);
        }

        if (!localDownloads[id]) {
          localDownloads[id] = [];
        }

        localDownloads[id].push({
          format: asset.format,
          label: asset.label,
          url: paths.rel,
        });
        count += 1;
      } catch (error) {
        failures.push(`${id}: ${asset.url} (${error.message})`);
      }
    }
  }

  fs.writeFileSync(OUTPUT_MAP_PATH, renderOutput(localDownloads), 'utf8');

  console.log(
    `Mirrored ${count} logo download assets to ${path.relative(process.cwd(), OUTPUT_DIR)}`,
  );
  console.log(
    `Wrote local map to ${path.relative(process.cwd(), OUTPUT_MAP_PATH)}`,
  );

  if (failures.length) {
    console.log('');
    console.log('Failed assets:');
    for (const failure of failures) {
      console.log(`- ${failure}`);
    }
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
